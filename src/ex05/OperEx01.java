package ex05;

public class OperEx01 {
    public static void main(String[] args) {
        System.out.println(1 + 1); // 더하기
        System.out.println(1 - 1); // 뺴기
        System.out.println(2 * 2); // 곱하기
        System.out.println(2 / 2); // 나누기 (몫만 출력)
        System.out.println(9 % 7); // 나눈후 나머지값만 출력

        // (산술더하기 or 결합)
        System.out.println("1" + 1);
    }
}
