package ex20;

public class LibMath {
    public static void main(String[] args) {
        int a = Math.abs(10);
        double b = Math.acos(10);
        double c = Math.asin(10);
        double d = Math.atan(10);
        double e = Math.atan2(10, 20);
        double f = Math.cos(10);
        double g = Math.cosh(10);
        double h = Math.exp(10);
        double i = Math.hypot(10, 20);
        double j = Math.log(10);
        double k = Math.log10(10);
        double l = Math.max(10, 20);
        double m = Math.min(10, 20);
        double o = Math.pow(10, 20);
        double p = Math.random();
        double q = Math.sin(10);
        double r = Math.sinh(10);
        double s = Math.sqrt(10);
        double t = Math.tan(10);
        double u = Math.tanh(10);
        double v = Math.toDegrees(10);
        double w = Math.toRadians(10);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);
        System.out.println(l);
        System.out.println(m);
        System.out.println(o);
        System.out.println(p);
        System.out.println(q);
        System.out.println(r);
        System.out.println(s);
        System.out.println(t);
        System.out.println(u);
        System.out.println(v);
        System.out.println(w);
    }
}
